// ── Model slug → display name ──────────────────────────────────────────────
function modelDisplay(slug) {
  return slug
    .split('-')
    .map(w => w.toUpperCase().length <= 3 ? w.toUpperCase() : w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ')
    .replace(/\b(Ev|Bz|Crv|Hrv|Cx\d)\b/gi, s => s.toUpperCase());
}

// ── Year note in Hebrew ────────────────────────────────────────────────────
function yearNote(year) {
  if (year >= 2025) return `עם הטכנולוגיה המתקדמת ביותר של ${year}`;
  if (year >= 2022) return `דגם ${year} עם שיפורים משמעותיים`;
  if (year >= 2018) return `דגם ${year} הבוגר`;
  if (year >= 2014) return `דגם ${year} שצבר ניסיון`;
  return `הגרסה הקלאסית של ${year}`;
}

