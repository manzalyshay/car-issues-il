// ── Pick 2 pros/cons with variation by year ────────────────────────────────
function pickItems(arr, year, offset = 0) {
  const n = arr.length;
  const i = ((year + offset) % n + n) % n;
  return [arr[i], arr[(i + 1) % n]];
}

// ── Build review payload ────────────────────────────────────────────────────
function buildReview(makeSlug, modelSlug, year) {
  const p = PROFILES[makeSlug] ?? GENERIC_PROFILE;
  const model = modelDisplay(modelSlug);
  const ynote = yearNote(year);

  const pros = pickItems(p.pros, year, 0);
  const cons = pickItems(p.cons, year, 1);

  const localSummaryHe  = p.local(model, year);
  const globalSummaryHe = p.global(model, year);

  // Main summary = local perspective (matches scrapeExpertReviews behaviour)
  const summaryHe = localSummaryHe;

  // Slight score adjustment by year: recent models get +0.1, very old -0.1
  const currentYear = new Date().getFullYear();
  const age = currentYear - year;
  const ageMod = age <= 2 ? 0.1 : age >= 8 ? -0.1 : 0;

  return {
    makeSlug,
    modelSlug,
    year,
    summaryHe,
    localSummaryHe,
    globalSummaryHe,
    localScore:  Math.min(10, Math.max(1, +(p.localScore  + ageMod).toFixed(1))),
    globalScore: Math.min(10, Math.max(1, +(p.globalScore + ageMod).toFixed(1))),
    pros,
    cons,
  };
}

// ── API helpers ─────────────────────────────────────────────────────────────
async function getMissing() {
  const res = await fetch(`${BASE_URL}/api/admin/bulk-generate-reviews`, { headers });
  if (!res.ok) throw new Error(`GET missing failed: ${res.status}`);
  const { missing, total } = await res.json();
  console.log(`Found ${total} missing year-specific reviews`);
  return missing;
}

async function insertOne(payload) {
  const res = await fetch(`${BASE_URL}/api/admin/insert-reviews`, {
    method: 'POST',
    headers,
    body: JSON.stringify(payload),
  });
  const data = await res.json().catch(() => ({}));
  return { ok: res.ok, ...data };
}

// ── Parallel runner ─────────────────────────────────────────────────────────
async function runBatch(items, concurrency) {
  let done = 0, failed = 0;
  const total = items.length;
  const queue = [...items];

  async function worker() {
    while (queue.length > 0) {
      const item = queue.shift();
      if (!item) break;
      try {
        const payload = buildReview(item.makeSlug, item.modelSlug, item.year);
        const result  = await insertOne(payload);
        done++;
        const pct = ((done + failed) / total * 100).toFixed(1);
        if (result.skipped)  console.log(`[${pct}%] SKIP ${item.makeSlug}/${item.modelSlug}/${item.year}`);
        else if (result.inserted) console.log(`[${pct}%] OK   ${item.makeSlug}/${item.modelSlug}/${item.year}`);
        else { console.log(`[${pct}%] ERR  ${item.makeSlug}/${item.modelSlug}/${item.year} — ${JSON.stringify(result)}`); failed++; }
      } catch (e) {
        failed++; done++;
        console.error(`FAIL ${item.makeSlug}/${item.modelSlug}/${item.year}: ${e.message}`);
      }
    }
  }

  await Promise.all(Array.from({ length: concurrency }, () => worker()));
  console.log(`\nDone: ${done - failed} inserted/skipped, ${failed} failed out of ${total}`);
}

// ── Main ────────────────────────────────────────────────────────────────────
const missing = await getMissing();
if (missing.length === 0) { console.log('Nothing to generate!'); process.exit(0); }
await runBatch(missing, CONCURRENCY);
